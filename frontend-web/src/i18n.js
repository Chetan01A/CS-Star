Object.entries(settingsTranslationOverrides).forEach(([code, overrides]) => {
  if (translations[code]) {
    Object.assign(translations[code], overrides);
  }
});

export const resolveLanguage = (code) => {
  if (!code) return 'en';
  if (translations[code]) return code;
  const baseCode = code.split('-')[0];
  if (translations[baseCode]) return baseCode;
  return 'en';
};

export const translate = (language, key, replacements = {}) => {
  const resolved = resolveLanguage(language);
  const template = translations[resolved]?.[key] ?? translations.en[key] ?? key;
  return Object.entries(replacements).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template
  );
};
